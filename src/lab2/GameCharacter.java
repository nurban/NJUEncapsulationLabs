public class GameCharacter {
     String firstName;
     String lastName;
     String species;
     String planet;
    
    //Setter for firstName
    public void getFirstName(String first) {
        this.firstName = first;
    }
    
    //Setter for lastName
    public void getLastName(String last) {
        this.lastName = last;
    }
    
    //Setter for species
    public void getSpecies(String species) {
        this.species = species;
    }
    
    //Setter for planet
    public void getPlanet(String planet) {
        this.planet = planet;
    }
}
